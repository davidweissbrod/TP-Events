class Users{
    id_user;
    first_name;
    last_name;
    username;
    password;

    Users(id, first, last, user, pass){
        this.id_user = id;
        this.first_name = first;
        this.last_name = last;
        this.username = user;
        this.password = pass; 
    }
}

export default Users;